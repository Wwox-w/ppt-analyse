#!/bin/bash
# ============================================
# 🛑 PPT 分析 Agent - 关闭服务脚本
# ============================================

echo "========================================"
echo "  🛑 正在关闭 AI 教授服务..."
echo "========================================"

# 查找占用 8000 端口的进程并关闭
PID=$(lsof -ti:8000)

if [ -z "$PID" ]; then
    echo "✅ 服务未在运行"
else
    echo "   找到进程 PID: $PID"
    echo "   正在关闭..."
    kill -9 $PID 2>/dev/null
    sleep 1
    
    # 确认是否关闭成功
    if lsof -ti:8000 > /dev/null 2>&1; then
        echo "❌ 关闭失败，请手动检查"
        exit 1
    else
        echo "✅ 服务已成功关闭！"
    fi
fi

echo ""
echo "========================================"
echo "  📖 下次启动请运行: ./start.sh"
echo "========================================"
